package basics;

public class WennDannSonst {

	public static void main(String[] args) {
		// wenn - dann
		if (1 < 3) {
			System.out.println("1 < 3");
		}
		//
		boolean trifft_zu = true;
		if (trifft_zu == true) {
			System.out.println("juhu");
		}
		// wenn dann sonst
		int zahl = 3;
		if (zahl == 1) {
			System.out.println("ungerade");
		} else {
			System.out.println("gerade");
		}
		//
		
		
	}

}
